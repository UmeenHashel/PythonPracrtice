#two types
#1. Explicit(manual)
#2. Implicit(automatically)

name = "Cap"
age = 24
gpa = 3.5
student = True

#int to float
age = float(age)
print(age)

#float to int
gpa = int(gpa)
print(gpa)

#cant convert string to int or float

#bool to int
student = int(student)
print(student)

#bool to float
student = float(student)
print(student)

#int to bool
age = bool(age)
print(age)

#float to bool
gpa = bool(gpa)
print(gpa)

#string to bool
name = bool(name)
print(name)

#if age = 0, gpa = 0.0 and name = " ", it'll be false


